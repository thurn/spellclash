// > 601.2a. To propose the casting of a spell, a player first moves that card
// > (or that copy of a card) from where it is to the stack. It becomes the
// > topmost object on the stack. It has all the characteristics of the card (or
// > the copy of a card) associated  with it, and that player becomes its
// > controller.
// <https://yawgatog.com/resources/magic-rules/#R6012a>

// > 601.2b. If the spell is modal, the player announces the mode choice (see
// > rule 700.2).
// <https://yawgatog.com/resources/magic-rules/#R6012b>

// > 601.2b. If the player wishes to splice any cards onto the spell (see rule
// > 702.47), they reveal those cards in their hand.
// <https://yawgatog.com/resources/magic-rules/#R6012b>

// > 601.2b. If the spell has alternative or additional costs that will be paid
// > as it's being cast such as buyback or kicker costs (see rules 118.8 and
// > 118.9), the player announces their intentions to pay any or all of those
// > costs (see rule 601.2f). A player can't apply two alternative methods of
// > casting or two alternative costs to a single spell.
// <https://yawgatog.com/resources/magic-rules/#R6012b>

// > 601.2b. If the spell has a variable cost that will be paid as it's being
// > cast (such as an {X} in its mana cost; see rule 107.3), the player
// > announces the value of that variable. If the value of that variable is
// > defined in the text of the spell by a choice that player would make later
// > in the announcement or resolution of the spell, that player makes that
// > choice at this time instead of that later time.
// <https://yawgatog.com/resources/magic-rules/#R6012b>

// 601.2b. If a cost that will be paid as the spell is being cast includes
// hybrid mana symbols, the player announces the nonhybrid equivalent cost they
// intend to pay. If a cost that will be paid as the spell is being cast
// includes Phyrexian mana symbols, the player announces whether they intend to
// pay 2 life or a corresponding colored mana cost for each of those symbols.
// <https://yawgatog.com/resources/magic-rules/#R6012b>

// > 601.2c. The player announces their choice of an appropriate object or
// > player for each target the spell requires ... The same target can't be
// > chosen multiple times for any one instance of the word "target" on the
// > spell.
// <https://yawgatog.com/resources/magic-rules/#R6012c>

// > 601.2c. If any effects say that an object or player must be chosen as a
// > target, the player chooses targets so that they obey the maximum possible
// > number of such effects without violating any rules or effects that say that
// > an object or player can't be chosen as a target.
// <https://yawgatog.com/resources/magic-rules/#R6012c>

// > 601.2c. The chosen objects and/or players each become a target of that
// > spell. (Any abilities that trigger when those objects and/or players become
// > the target of a spell trigger at this point; they'll wait to be put on the
// > stack until the spell has finished being cast.)
// <https://yawgatog.com/resources/magic-rules/#R6012c>

// > 601.2d. If the spell requires the player to divide or distribute an effect
// > (such as damage or counters) among one or more targets, the player
// > announces the division. Each of these targets must receive at least one of
// > whatever is being divided.
// <https://yawgatog.com/resources/magic-rules/#R6012d>

// > 601.2g. If the total cost includes a mana payment, the player then has a
// > chance to activate mana abilities (see rule 605, "Mana Abilities").
// <https://yawgatog.com/resources/magic-rules/#R6012g

// > 601.2h. The player pays the total cost. First, they pay all costs that
// > don't involve random elements or moving objects from the library to a
// > public zone, in any order. Then they pay all remaining costs in any order.
// <https://yawgatog.com/resources/magic-rules/#R6012h
